#include <stdio.h>

int main() {
    // Kieu du lieu int: Dung de luu tru so nguyen
    int age = 25;  // Khai bao bien age kieu int va khoi tao gia tri 25
    
    // Kieu du lieu float: Dung de luu tru so thuc (so co phan thap phan)
    float weight = 70.5;  // Khai bao bien weight kieu float va khoi tao gia tri 70.5
    
    // Kieu du lieu double: Dung de luu tru so thuc voi do chinh xac cao hon float
    double height = 175.75;  // Khai bao bien height kieu double va khoi tao gia tri 175.75
    
    // Kieu du lieu char: Dung de luu tru ky tu (1 byte)
    char grade = 'A';  // Khai bao bien grade kieu char va khoi tao gia tri 'A'
    
    // Kieu du lieu long: Dung de luu tru so nguyen voi dung luong lon hon int
    long population = 8000000L;  // Khai bao bien population kieu long va khoi tao gia tri 8000000
    
    // Kieu du lieu short: Dung de luu tru so nguyen voi dung luong nho hon int
    short days = 365;  // Khai bao bien days kieu short va khoi tao gia tri 365
    
    // Kieu du lieu unsigned int: Dung de luu tru so nguyen khong dau (chi gia tri duong)
    unsigned int count = 100;  // Khai bao bien count kieu unsigned int va khoi tao gia tri 100
    
    // Kieu du lieu boolean (C khong co kieu boolean san, nhung co the dung int voi gia tri 0 la false, khac 0 la true)
    int isActive = 1;  // Khai bao bien isActive kieu int va khoi tao gia tri 1 (true)
    
    return 0;
}


